package account_3;


public class ContactInfo {

    private String details;

    //CONSTRUCTOR
    public ContactInfo() {}

    public ContactInfo(String details) {
        this.details = details;
    }


    //GETTER - SETTER
    public String getDetails() {
        return details;
    }

    public void setDetails(String details) {
        this.details = details;
    }
}
